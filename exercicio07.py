""" Faça um Programa que calcule a área de um quadrado,
em seguida mostre o dobro desta área para o usuário."""

lado_01 = int(input("Digite o primeiro lado: "))
lado_02 = int(input("Digite o segundo lado: "))

area_do_quadrado = (lado_02 * lado_01)*2
print("Área do quadrado {}m²".format(area_do_quadrado))